function lit(v) {
  if (v === null || v === undefined) return 'NULL';
  return `$SOP$${String(v)}$SOP$`;
}
function jlit(v) {
  if (v === null || v === undefined) return 'NULL';
  return `$SOP$${JSON.stringify(v)}$SOP$::jsonb`;
}

